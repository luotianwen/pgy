package com.kkgame.feeop.data.bean;

public class UserAdVO {

	private String statDate;
	
	private int type;
	
	private int country;
	
	private String countryName;
	
	private int noUserCount;
	
	private int allUserCount;
	
	private int yesUserCount;
	
	private int totalUserCount;
	
	private double noPercent;
	
	private double yesPercent;
	
	private double allPercent;

	public String getStatDate() {
		return statDate;
	}

	public void setStatDate(String statDate) {
		this.statDate = statDate;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getCountry() {
		return country;
	}

	public void setCountry(int country) {
		this.country = country;
	}

	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	public int getNoUserCount() {
		return noUserCount;
	}

	public void setNoUserCount(int noUserCount) {
		this.noUserCount = noUserCount;
	}

	public int getAllUserCount() {
		return allUserCount;
	}

	public void setAllUserCount(int allUserCount) {
		this.allUserCount = allUserCount;
	}

	public int getYesUserCount() {
		return yesUserCount;
	}

	public void setYesUserCount(int yesUserCount) {
		this.yesUserCount = yesUserCount;
	}

	public int getTotalUserCount() {
		return totalUserCount;
	}

	public void setTotalUserCount(int totalUserCount) {
		this.totalUserCount = totalUserCount;
	}

	public double getNoPercent() {
		return noPercent;
	}

	public void setNoPercent(double noPercent) {
		this.noPercent = noPercent;
	}

	public double getYesPercent() {
		return yesPercent;
	}

	public void setYesPercent(double yesPercent) {
		this.yesPercent = yesPercent;
	}

	public double getAllPercent() {
		return allPercent;
	}

	public void setAllPercent(double allPercent) {
		this.allPercent = allPercent;
	}
}
